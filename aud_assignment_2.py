"""The top 50 words are identified from the scraped reviews. The frequency scoring for the words are such that if a word appaears multiple times in a review, it is only counted as one. In short, for each word the counts depict in how many reviews that wword appears.

The attributes for the beer based on which beers are to be recommended are chosen from the top 50 words that appear in the scraped reviews.

**Attributes Identification and Categorization**




The Attributes that a beer can have can be broadly divided into 3 categories: The attributes are identified from the top 50 words and categorized into these 3 categories.

**Appearance** : Based on color of the beer (dark, light, black, white, orange)

**Flavor** : Based on the taste of the beer (bitterness, sweet, malt, coffee, vanilla, chocolate, citrus, bourbon, fruit)

**Mouthfeel/Texture**: Based on the texture of the beer or the feel while drinking the beer (smooth, thick ,lacing, carbonation)

Next, a lift analysis is done among these 18 potential attributes and 3 attributes are selected from these 18 which are then considered to be the user's specification of attributes for a beer.

**Lift calculation for the attributes of interest**
"""

N = len(df)  # total reviews

# attributes of interest
attributes = ["sweet", "smooth", "dark","light","white","black","carbonation","malt","lacing","thick","orange","coffee","vanilla",
              "chocolate","citrus","bourbon","fruit","bitterness"]

# frequency per attribute (#reviews containing the word)
attr_counts = {
    w: sum(1 for tokens in df["tokens"] if w in tokens)
    for w in attributes
}

from itertools import combinations

# co-occurrence counts between attributes
co_counts = {
    (w1, w2): sum(1 for tokens in df["tokens"] if w1 in tokens and w2 in tokens)
    for w1, w2 in combinations(attributes, 2)
}

#lift calculation
lift_results = []

for (w1, w2), co in co_counts.items():
    p_w1 = attr_counts[w1] / N
    p_w2 = attr_counts[w2] / N
    p_w1w2 = co / N

    if p_w1 > 0 and p_w2 > 0:  # avoid division by zero
        lift = p_w1w2 / (p_w1 * p_w2)
    else:
        lift = None

    lift_results.append({
        "word1": w1,
        "word2": w2,
        "count_w1": attr_counts[w1],
        "count_w2": attr_counts[w2],
        "co_count": co,
        "lift": lift
    })

lift_df = pd.DataFrame(lift_results)
lift_df=lift_df.sort_values(by='lift',ascending=False)
