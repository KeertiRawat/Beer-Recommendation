def parse_subscores(text):
    # e.g. "look: 4.5 | smell: 4.75 | taste: 4.25 | feel: 4 | overall: 5"
    def grab(lbl):
        m = re.search(rf"{lbl}\s*:\s*([0-9.]+)", text, flags=re.I)
        return float(m.group(1)) if m else None
    return grab("look"), grab("smell"), grab("taste"), grab("feel"), grab("overall")


def clean_review_text_fast(t: str) -> str:
    if not t:
        return ""
    # normalize whitespace quickly (faster than regex)
    t = " ".join(t.split())

    # quick skips
    low = t.lower()
    if low.startswith("rated:") or low.startswith("reviewed by:"):
        return ""

    # remove inline sections we never want
    cut_idx = len(t)
    for kw in ("Image:", "View:"):
        k = t.find(kw)
        if k != -1:
            cut_idx = min(cut_idx, k)
    t = t[:cut_idx].rstrip()

    # drop trailing "Report"
    if t.endswith(" Report"):
        t = t[:-7].rstrip()

    # drop trailing date like "Mar 25, 2025"
    # quick scan from the end; no regex needed
    parts = t.rsplit(" ", 3)  # try to catch "... Mon DD, YYYY"
    if len(parts) == 4 and parts[-3][:3] in MONTHS and parts[-2].rstrip(",").isdigit() and parts[-1].isdigit():
        # confirm it looks like "Mon DD, YYYY"
        t = " ".join(parts[:-3]).rstrip()

    return t



CSS_REVIEW_BLOCKS = "#rating_fullview_content_2"

def clean_review_text_fast(s: str) -> str:
    if not s: return ""
    s = " ".join(s.split())
    if s.endswith(" Report"):
        s = s[:-7].rstrip()
    s = RE_DATE.sub("", s)
    s = s.replace("Image:", "").replace("View:", "")
    return s.strip(" .:-\n\t")

def extract_reviews_from_page(driver, beer_base_link):
    WebDriverWait(driver, 4, poll_frequency=0.15).until(
        EC.presence_of_all_elements_located((By.CSS_SELECTOR, CSS_REVIEW_BLOCKS))
    )
    blocks = driver.find_elements(By.CSS_SELECTOR, CSS_REVIEW_BLOCKS)

    rows = []
    for b in blocks:
        text = (b.get_attribute("innerText") or "").strip()
        if not text:
            continue

        # reviewer_overall (normalized)
        m_overall = RE_OVERALL_NEAR_5.search(text)
        reviewer_overall = float(m_overall.group(1)) if m_overall else None

        # subscores
        def grab(lbl):
            m = RE_SUB(lbl).search(text);  return float(m.group(1)) if m else None
        look  = grab("look");  smell = grab("smell");  taste = grab("taste")
        feel  = grab("feel");  overall_component = grab("overall")


        # --- derive review text from the block (strict filtering) ---
        lines = [ln.strip() for ln in text.splitlines() if ln.strip()]

        # drop meta & subscore lines entirely
        content_lines = [ln for ln in lines if not RE_META_LINE.match(ln) and not RE_SUBSCORE_LN.search(ln)]
        if not content_lines:
            continue

        # take the longest remaining content (or join; longest is usually fine)
        candidate = max(content_lines, key=len)
        # candidate = " ".join(content_lines)  # <- use this instead if you prefer to merge lines
        review_text = clean_review_text_fast(candidate)

        # final guard
        if RE_META_LINE.match(review_text) or RE_SUBSCORE_LN.search(review_text):
            continue



        #Append all the rows
        if review_text and len(review_text) >= MIN_REVIEW_LEN:
            rows.append({
                "beer_link": beer_base_link,
                "reviewer_overall": reviewer_overall,
                "look": look, "smell": smell, "taste": taste, "feel": feel,
                "overall_component": overall_component,
                "review_text": review_text
            })

    return rows



def find_next_url(driver):
    """
    Generic Next finder:
    - Find the pagination bar under #ba-content
    - Return the href of the anchor whose visible text contains 'next' (case-insensitive)
    - If 'next' is muted (final page), it's a <span>, so we return None
    """
    # Grab all anchors inside the pagination span; ignore indexes entirely
    anchors = driver.find_elements(By.XPATH, "//*[@id='ba-content']//span//a")
    for a in anchors:
        label = a.text.strip().lower()
        if "next" in label:
            href = a.get_attribute("href")
            if href:
                return urljoin(driver.current_url, href)
    return None  # no next (either last page or pagination missing)


def scrape_one_beer_reviews(driver, beer_base_link):
    driver.get(beer_base_link)

    collected = []
    visited = set()

    while True:
        cur = driver.current_url
        if cur in visited:
            break  # safety: avoid loops if site glitches back to same page
        visited.add(cur)

        rows = extract_reviews_from_page(driver, beer_base_link)
        need = LIMIT_PER_BEER - len(collected)
        if need > 0:
            collected.extend(rows[:need])

        if len(collected) >= LIMIT_PER_BEER:
            break

        next_url = find_next_url(driver)
        if not next_url:
            break
        driver.get(next_url)

    return pd.DataFrame(collected, columns=["beer_link","reviewer_overall","look","smell","taste","feel","overall_component","review_text"])

