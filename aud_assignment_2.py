    out = []
    for r in rows:
        a = r.find_element(By.XPATH, ".//td/a[starts-with(@href,'/beer/profile/')]")
        beer_link = a.get_attribute("href")
        try:
            beer_name = a.find_element(By.TAG_NAME, "b").text.strip()
        except:
            beer_name = a.text.strip()

        span = r.find_element(By.XPATH, ".//td/span[@class='muted']")
        brewer_a = span.find_elements(By.XPATH, ".//a[starts-with(@href,'/beer/profile/')]")
