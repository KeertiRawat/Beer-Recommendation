"""Train Word2Vec on the 10 beers"""

sentences = df10["tokens"].tolist()

w2v = Word2Vec(
    sentences=sentences,
    vector_size=100,
    window=5,
    min_count=2,
    workers=4,
    sg=1  # skip-gram
)

"""Build one vector per beer (average embeddings of selected attributes)"""

beer_vectors = {}

for beer, group in df10.groupby("beer_name"):
    # flatten tokens for this beer
    flat = [tok for toks in group["tokens"] for tok in toks]
    counts = Counter(t for t in flat if t in selected_attributes and t in w2v.wv.key_to_index)
    if not counts:
        # fallback: zero vector if no selected attributes found
        beer_vectors[beer] = np.zeros(w2v.vector_size, dtype=float)
        continue
    vec_sum = np.zeros(w2v.vector_size, dtype=float)
    weight_sum = 0.0
    for term, freq in counts.items():
        vec_sum += w2v.wv[term] * float(freq)
        weight_sum += float(freq)
    beer_vectors[beer] = vec_sum / weight_sum

# Build matrix in the same beer order:
beer_names_embed = list(beer_vectors.keys())
beer_matrix_embed = np.vstack([beer_vectors[b] for b in beer_names_embed])

print("beer_matrix_embed shape:", beer_matrix_embed.shape)

