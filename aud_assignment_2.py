df["tokens"] = df["review_text"].apply(preprocess)

"""We now want to recommend beers based on the attributes- Black, Bourbon and Thick. Instead of recommending by raw word overlap, we build a representation of each beer from its reviews. We then combine semantic information (via Custom Word2Vec embeddings) with sentiment (so positive mentions count more than negative), And finally, we compare beers and attributes using cosine similarity."""

# Setup
nltk.download("vader_lexicon")
sia = SentimentIntensityAnalyzer()

# Attributes of interest
attributes = ["black", "thick", "bourbon"]

"""Now, we are training custom embeddings. The input is df['tokens'].tolist() which is each review preprocessed into a list of tokens. The Word2Vec model learns vector representation (vector size = 100) of words based on how they co-occur in the reviews. They key idea is that words that appear in similar contexts (eg 'citrus','orange','grapefruit') will have similar vectors. So this let's us capture domain-specific semantics from the beer review dataset (not generic language)

"""

# Train custom embeddings

# Assuming df["tokens"] exists from preprocessing
sentences = df["tokens"].tolist()

w2v_model = Word2Vec(
    sentences,
    vector_size=100,
    window=5,
    min_count=2,
    workers=4,
    sg=1  # skip-gram
)

"""Next, for each review we scan tokens- looking for attributes which are 'Black','Bourbon','Thick'. For each mention, extracting its local context (a 7-word window) and compute VADER sentiment. The compund score ranges from -1 to +1. We sum these sentiments values across all mentions in the review (sentiment_sum). We are doing sum and not average because multiple positive mentions strengthen the signal. Then, for each attribute mention, fetch its Word2Vec embeddings and take the mean of embeddings (so a review has one vector summarizing the attributed mentioned). Next, we multiply the averaged embedding by the summed sentiment score. Positive reviews push the vector outward (strong signal) and negative reviews pull it back (weak signal). The final result will be one sentiment-weighted vector per review that encodes both semantic meaning and customer opinion."""

# Review-level representation

def get_review_vector(tokens, attributes, model, sia):
    attr_vecs = []
    sentiment_sum = 0.0

    for i, token in enumerate(tokens):
        if token in attributes and token in model.wv:
            # local context for sentiment
            context = " ".join(tokens[max(0, i-3): i+4])
            sentiment = sia.polarity_scores(context)["compound"]

            sentiment_sum += sentiment
            attr_vecs.append(model.wv[token])

    if len(attr_vecs) == 0:
        return None  # no attributes mentioned in this review

    #average embeddings of mentioned attributes
    avg_attr_vec = np.mean(attr_vecs, axis=0)

    #weight by summed sentiment score
    return avg_attr_vec * sentiment_sum

# Apply to each review
df["review_vector"] = df["tokens"].apply(
    lambda t: get_review_vector(t, attributes, w2v_model, sia)
)

"""Each beer has multiple reviews. For each beer, we average all review vectors (ignoring reviews with no attributes mentioned). This gives one beer profile vector- which captures all mentions of the attributes across reviewers and weighs them by sentiment strength. So the beer vector = Summation (Review Embedding X Sentiment Sum)/ Number of relevant reviews


"""

# Beer-level aggregation

beer_profiles_embed = {}

for beer, group in df.groupby("beer_name"):
    valid_vecs = [v for v in group["review_vector"] if v is not None]
    if len(valid_vecs) > 0:
        beer_profiles_embed[beer] = np.mean(valid_vecs, axis=0)

#Create beer matrix for similarity
beer_names_embed = list(beer_profiles_embed.keys())
beer_matrix_embed = np.vstack(list(beer_profiles_embed.values()))

"""For each of the attributes- Black, Bourbon and Thick, we then check if in vocabulary then we use its embedding, is not then use zeros. We then average the query vector, and no sentiment weighting here becuase the user query is a neutral preference.

Next, we check cosine similarity which measures the angle between query vector and beer profile vectors. The inutuition here is is a beer's reviews are full of positive mentions, its profile vector will be aligned with the query vector. Thus higher similarity = better recommendation.
"""

# Recommender function

def recommend_beers_embed(query_attrs, top_k=3):
    query_vecs = []
    sentiment_dummy = 1.0  # for query we don't have review sentiment

    for attr in attributes:
        if attr in query_attrs and attr in w2v_model.wv:
            query_vecs.append(w2v_model.wv[attr])
        else:
            query_vecs.append(np.zeros(w2v_model.vector_size))

    query_vector = np.mean(query_vecs, axis=0) * sentiment_dummy
    query_vector = query_vector.reshape(1, -1)

    sims = cosine_similarity(query_vector, beer_matrix_embed)[0]
    results = pd.DataFrame({"beer_name": beer_names_embed, "similarity": sims})
    return results.sort_values(by="similarity", ascending=False).head(top_k)

