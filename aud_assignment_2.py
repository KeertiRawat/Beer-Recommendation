"""####
The lift analysis is helpful as it helps to identify which attributes are not to be chosen together while a user is mentioning his/her preferences:

(In general, more the co-mentions or lift value, greater is the chance that these attributes are closely related to each other)

For example,(white, orange) - lift of 3.41 but it doesn't make sense to give 2 attributes that fall under the appearance category.

Another such case is the combination of (coffee,chocolate) - lift of 3.16.

(coffee, citrus) - lift of 0.22, Also chances are very less that a beer can have both have a coffee and a citrus flavor.

(white,black) - lift of 0.195 . Also makes sense, as a beer can't be white and black at the same time.

From the lift analysis, the attributes are chosen for recommendation in such a manner that the l**ift values among the pairs are high** and also the attributes fall into the **3 defined categories**.

The 3 attributes chosen for the recommendation are **black**, **thick** and **bourbon**. These attributes fall into the **appearance**, **texture** and **flavor** categories respectively and the lift values for the pairs are:

**(black,thick) =3.01, (thick,bourbon) =1.75, (black,bourbon)=2.77**

#### **Building the Recommender System based on BoW, Sentiment Analysis and Cosine Similarity**

The Recommendation System is built in a 3 stage process:

1. **Bag of Words(BoW)** - The user query/ preference vector is based on the attributes specified ['white','lacing','citrus']. So for each review for each beer, the BoW representation of these attributes is just a presence or absence vector (0/1) i.e it will be of the form [1/0,1/0,1/0]. This approach ensures that reviews with less mentions of these attributes and reviews that overly mention these attributes have a same weightage in terms of the BoW representation.

2. **Sentiment Analysis** - For each review for each beer, if each of these 3 attributes are mentioned multiple times say white (5 times), lacing (3 times) and citrus (2 times), the sum value of the weighted sentiment associated with that attribute is calculated to a get a sentiment score for each of the attributes. A sentiment score vector is then obtained for each review for each beer say [0.4,0.7, 0.1]. The sentiment analyzer used here is VADER. The sentiment scoring approach is a weighted scoring approach such that as the distance of the word describing the attribute mention increases, that attribute mention gets a lower sentiment score.



*   Once, the BoW and Sentiment Analysis is done for each review and for each beer, the product of the BoW and Sentiment Scores are taken to get a combined vector.

*   This combined vector is then aggregated across multiple reviews of a beer by taking the average of the combined vectors. (As averaging ensures that beers with less reviews and beers with more reviews are treated similarly as opposed to summing the combined vectors across the reviews). However, while averaging, only the reviews where any of these 3 attributes that are mentioned are considered. (Say if there are 100 reviews for a beer, only 20 reviews would have these attribute mentions. So while averaging, 20 reviews are considered instead of the whole 100 reviews for that beer.)


3. **Cosine Similarity** - Finally, the cosine similarity is computed for each of the beer vectors and the user query/user preference vector (which is of the form [1,1,1]) and the beers that have the highest cosine similarity are recommended to the user.
"""

